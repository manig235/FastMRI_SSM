class VarNetBlock(nn.Module):
    """
    Model block for end-to-end variational network.

    This model applies a combination of soft data consistency with the input
    model as a regularizer. A series of these blocks can be stacked to form
    the full variational network.
    """

    def __init__(self, model: nn.Module):
        """
        Args:
            model: Module for "regularization" component of variational
                network.
        """
        super().__init__()

        self.model = model
        self.dc_weight = nn.Parameter(torch.ones(1))

    def sens_expand(self, x: torch.Tensor, sens_maps: torch.Tensor) -> torch.Tensor:
        return fastmri.fft2c(fastmri.complex_mul(x, sens_maps))

    def sens_reduce(self, x: torch.Tensor, sens_maps: torch.Tensor) -> torch.Tensor:
        x = fastmri.ifft2c(x)
        return fastmri.complex_mul(x, fastmri.complex_conj(sens_maps)).sum(
            dim=1, keepdim=True
        )

    def forward(
        self,
        current_kspace: torch.Tensor,
        ref_kspace: torch.Tensor,
        mask: torch.Tensor,
        sens_maps: torch.Tensor,
    ) -> torch.Tensor:
        zero = torch.zeros(1, 1, 1, 1, 1).to(current_kspace)
        soft_dc = torch.where(mask, current_kspace - ref_kspace, zero) * self.dc_weight
        model_term = self.sens_expand(
            self.model(self.sens_reduce(current_kspace, sens_maps)), sens_maps
        )

        return current_kspace - soft_dc - model_term

class AttentionBlock(nn.Module):
    def __init__(self, in_chans, out_chans, alpha = 0.5):
        super().__init__()
        self.in_chans = in_chans
        self.out_chans = out_chans
        self.alpha = alpha
        self.beta = 1-self.alpha
        self.sigmoid = nn.Sigmoid()
        self.pooling = nn.AdaptiveAvgPool2d((1,1))
        self.channel_attention = nn.Sequential(
#             nn.AdaptiveAvgPool2d((1,1)),
            nn.Linear(in_chans, in_chans//4),
            nn.ReLU(),
            nn.Linear(in_chans//4, in_chans),
            nn.Sigmoid()
        )   
        self.conv_f = nn.Conv2d(in_chans, in_chans//4, stride = 1, kernel_size=1)
        self.conv_g = nn.Conv2d(in_chans, in_chans//4, stride = 1, kernel_size=1)
        self.conv_h = nn.Conv2d(in_chans, in_chans, stride = 1, kernel_size= 1)
        self.conv_v = nn.Conv2d(in_chans, in_chans, stride = 1, kernel_size= 1)
        self.gamma = nn.Parameter(torch.tensor(0.))

    
    def forward(self, input):
        b,c,h,w = input.shape
        temp = self.pooling(input).squeeze(2).squeeze(2)
        output_channel = self.channel_attention(temp)
        output_channel = output_channel.unsqueeze(2).unsqueeze(2)
        print(output_channel.shape)
        output_channel = F.upsample(output_channel, size = (h,w))
        output_f = self.conv_f(input).view(b,c,-1)
        output_g = self.conv_g(input).view(b,c,-1)
        inner_prod = output_f.transpose(1,2) @ output_g
        soft_prod = F.softmax(inner_prod, dim = 1)
        output_h = self.conv_h(input).view(b,c,-1)
        attentioned_output = output_h @ soft_prod
        attentioned_output = attentioned_output.view(b,c,h,w)
        output_self = self.conv_v(attentioned_output)
        output_self = output_self * self.gamma + input
        output_fusion = self.alpha * output_channel + self.beta * output_channel
        output_scaled = self.sigmoid(output_fusion)
        output = input * output_scaled
        return output

